/**
 * Shared shell dimensions for {@link Button} and sibling controls that
 * should read as the same labelled pill (e.g. {@link "../ToggleButton".ToggleButton}).
 * Centralised here so padding, radius, and label rhythm stay in sync when
 * either control is tuned.
 */

/** Corner radius applied to {@link Button}'s pill shell (logical px). */
export const BUTTON_BORDER_RADIUS_PX = 16;

/** Horizontal padding inside {@link Button}'s pill (logical px). */
export const BUTTON_PADDING_HORIZONTAL_PX = 12;

/** Vertical padding inside {@link Button}'s pill (logical px). */
export const BUTTON_PADDING_VERTICAL_PX = 8;

/** Label font size for {@link Button} (logical px). */
export const BUTTON_LABEL_FONT_SIZE_PX = 14;

/** Label line height for {@link Button} (logical px). */
export const BUTTON_LABEL_LINE_HEIGHT_PX = 16;
